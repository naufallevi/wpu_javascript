var s = "";

for (var i = 0; i < 10; i++) {
  for (var k = 0; k < 10 - i;k++) {
    s+=" ";
  }

  for (var j = 0; j < 2 * i + 1; j++) {
    s += "*";
  }
  s += "\n";
}

for (var i = 9; i > 0; i--) {
  for (var k = 0; k < 10 - i + 1; k++ ) {
    s+=" ";
  }

  for (var j = 0; j < 2 * i - 1; j++) {
    s += "*";
  }
  s += "\n";
}

console.log(s);

